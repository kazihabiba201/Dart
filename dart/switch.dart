main() {
  int day = 9;

  switch (day) {
    case 1:
      print('Spring');
      break;
    case 2:
      print('Summer');
      break;
    case 3:
      print('Winter');
      break;
    case 4:
      print('Autumn');
      break;
    case 5:
      print('Monsoon');
      break;
    case 6:
      print('Pre Winter');
      break;
    case 7:
    case 8:
    case 9:
      print('Windy!');
      break;
    default:
      print('Wrong Season!');
  }
}
